void HAL_I2C_MasterRxCpltCallback(I2C_HandleTypeDef *hi2c)
{
	if (hi2c->Instance == I2C1)
	{
		temperature = temp[0]<<8 | temp[1];
		float vtemp = 25 + ((((voltages * VOLTAGE) / RESOLUTION) - 0.76 ) / 0.025);
		float temp_c = temperature/256.0;

		snprintf(str1, sizeof(str1), "T_EMB=%.3f C\n", vtemp);
		snprintf(str2, sizeof(str2), "T_I2C=%.3f C\n", temp_c);

		lcd_println(str1, 0);
		lcd_println(str2, 1);

		if (temp_c > vtemp)
		{
			snprintf(string, sizeof(string), "The highest sensor is the external one : %.3f C\n", temp_c);
			HAL_UART_Transmit_DMA(&huart2, (uint8_t*)string, sizeof(string));
		}

		else if (temp_c < vtemp)
		{
			snprintf(string, sizeof(string), "The highest sensor is the internal one : %.3f C\n", vtemp);
			HAL_UART_Transmit_DMA(&huart2, (uint8_t*)string, sizeof(string));
		}
		else
		{
			snprintf(string, sizeof(string), "They have the same value\n");
			HAL_UART_Transmit_DMA(&huart2, (uint8_t*)string, sizeof(string));
		}
	}
}

